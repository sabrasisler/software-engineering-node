/**
 * @file Declares Location data type representing latitude and longitude
 */

/**
 * @typedef Location Represents location in latitude and longitude
 * @property {number} latitude Latitude of a location
 * @property {number} longitude Longitude of a location
 */

export default class Location {
    public latitude: number = 0.0;
    public longitude: number = 0.0;
 };
 